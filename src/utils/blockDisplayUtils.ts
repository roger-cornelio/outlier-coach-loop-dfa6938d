/**
 * Verifica se uma linha Г© EXECUTГЃVEL (treino vГЎlido)
 * MVP0: Agora sГі verifica se comeГ§a com "- "
 */
export function isExecutableLine(line: string): boolean {
  const trimmed = line.trim();
  if (!trimmed) return true; // Linha vazia Г© "vГЎlida" (nГЈo bloqueia)
  return trimmed.startsWith('- ') || trimmed.startsWith('-');
}
