// 
// REGRA ABSOLUTA:
// - Preview, Publicação e Tela do Atleta NUNCA reparsam texto
// - Usam dados já estruturados do bloco (block.lines, block.coachNotes)
// - Não geram alertas, não validam sintaxe
// ════════════════════════════════════════════════════════════════════════════

/** Prefixo especial para linhas estruturais inline (múltiplos ROUNDS no mesmo bloco) */
export const STRUCT_LINE_PREFIX = '__STRUCT:';

export interface BlockDisplayData {
  /** Linhas de exercício para exibição (sem hífen). Pode conter __STRUCT: prefixadas para badges inline. */
  exerciseLines: string[];
  /** Notas do coach (comentários) */
  coachNotes: string[];
  /** Descrição da estrutura (ex: "3 ROUNDS", "EMOM 30 MIN") — só quando há UMA única estrutura */
  structureDescription: string | null;
  /** Se o bloco tem conteúdo renderizável */
  hasContent: boolean;
}

/**
 * Extrai dados de exibição a partir de um bloco JÁ PARSEADO.
 * 
 * REGRA ABSOLUTA:
 * - NÃO reparse texto
 * - NÃO gera alertas
 * - NÃO valida sintaxe
 * - Apenas formata para exibição
 * 
 * @param block - Objeto do bloco com lines, coachNotes, content
 * @returns Dados formatados para exibição (exerciseLines, coachNotes, structureDescription)
 */
export function getBlockDisplayDataFromParsed(block: {
  lines?: Array<{ text?: string; type?: string } | string>;
  coachNotes?: string[];
  content?: string;
}): BlockDisplayData {
  const exerciseLines: string[] = [];
  const coachNotes: string[] = [];
  let structureDescription: string | null = null;

  // ════════════════════════════════════════════════════════════════════════════
  // PRIORIDADE 1: Se block.lines contém strings brutas (rawLines do coach),
  // usar como fonte principal — garante 100% de fidelidade ao texto colado.
  // ════════════════════════════════════════════════════════════════════════════
  const hasRawStringLines = Array.isArray(block.lines) && block.lines.length > 0 
    && typeof block.lines[0] === 'string';
  
  if (hasRawStringLines) {
    // block.lines são strings brutas (rawLines preservadas)
    for (const line of block.lines as string[]) {
      const trimmed = (line || '').trim();
      if (!trimmed) continue;
      
      // Estrutura entre ** **
      const structLabel = normalizeStructureLabel(trimmed);
      if (structLabel) {
        structureDescription = structLabel;
        continue;
      }
      
      // Exercício com hífen - remover prefixo para exibição
      if (trimmed.startsWith('- ') || trimmed.startsWith('-')) {
        const cleanLine = trimmed.replace(/^-\s*/, '').trim();
        if (cleanLine) {
          exerciseLines.push(cleanLine);
        }
      } else if (isStructuralLine(trimmed)) {
        // Linha estrutural (TABATA, EMOM, AMRAP, etc.) sem ** ** → badge
        structureDescription = trimmed;
      } else {
        // Qualquer outra linha (instruções, sub-formatos, "Sendo", etc.)
        exerciseLines.push(trimmed);
      }
    }
    
    // Adicionar coachNotes do bloco
    if (Array.isArray(block.coachNotes) && block.coachNotes.length > 0) {
      for (const note of block.coachNotes) {
        if (note?.trim() && !coachNotes.includes(note.trim())) {
          coachNotes.push(note.trim());
        }
      }
    }
    
    const hasContent = exerciseLines.length > 0 || coachNotes.length > 0 || structureDescription !== null;
    return { exerciseLines, coachNotes, structureDescription, hasContent };
  }

  // ════════════════════════════════════════════════════════════════════════════
  // FALLBACK: block.lines são ParsedLine[] (objetos com text/type)
  // ════════════════════════════════════════════════════════════════════════════
  if (Array.isArray(block.lines) && block.lines.length > 0) {
    for (const line of block.lines) {
      const text = typeof line === 'string' ? line : (line?.text || '');
      const type = typeof line === 'string' ? 'exercise' : (line?.type || 'exercise');
      
      if (!text.trim()) continue;
      
      // Comentários vão para coachNotes
      if (type === 'comment') {
        coachNotes.push(text.trim());
        continue;
      }
      
      // Estruturas (**ROUNDS**, **EMOM**, etc.) - extrair descrição
      const structLabel = normalizeStructureLabel(text);
      if (structLabel) {
        structureDescription = structLabel;
        continue;
      }
      
      // Linha estrutural sem ** ** (TABATA, EMOM, etc.) → badge
      if (isStructuralLine(text.trim())) {
        structureDescription = text.trim();
        continue;
      }
      
      // Exercícios - remover hífen para exibição limpa
      const cleanLine = text.replace(/^-\s*/, '').trim();
      if (cleanLine) {
        exerciseLines.push(cleanLine);
      }
    }
  }
  
  // Adicionar coachNotes do bloco (já extraídos durante parse)
  if (Array.isArray(block.coachNotes) && block.coachNotes.length > 0) {
    for (const note of block.coachNotes) {
      if (note?.trim() && !coachNotes.includes(note.trim())) {
        coachNotes.push(note.trim());
      }
    }
  }
  
  // Fallback/complemento: usar block.content quando ele tem mais linhas que o parsed
  if (block.content) {
    const contentLines = block.content.split('\n');
    const contentExerciseLines: string[] = [];
    
    for (const line of contentLines) {
      const trimmed = line.trim();
      if (!trimmed) continue;
      
      // Estrutura entre ** **
      const structLabel = normalizeStructureLabel(trimmed);
      if (structLabel && !structureDescription) {
        structureDescription = structLabel;
        continue;
      }
      
      // Exercício com hífen - remover prefixo para exibição
      if (trimmed.startsWith('- ') || trimmed.startsWith('-')) {
        const cleanLine = trimmed.replace(/^-\s*/, '').trim();
        if (cleanLine) {
          contentExerciseLines.push(cleanLine);
        }
      } else if (isStructuralLine(trimmed)) {
        if (!structureDescription) {
          structureDescription = trimmed;
        }
      } else {
        contentExerciseLines.push(trimmed);
      }
    }
    
    // Se o content bruto tem MAIS linhas que o parsed, usar o content (é mais completo)
    if (contentExerciseLines.length > exerciseLines.length) {
      exerciseLines.length = 0;
      exerciseLines.push(...contentExerciseLines);
    }
  }

  const hasContent = exerciseLines.length > 0 || coachNotes.length > 0 || structureDescription !== null;

  return {
    exerciseLines,
    coachNotes,
    structureDescription,
    hasContent,
  };
}
